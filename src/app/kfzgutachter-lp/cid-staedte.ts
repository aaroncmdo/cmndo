/**
 * Liefert den Stadt-Namen für einen rohen `?cid=…`-Param-Wert.
 * Trimmt + numerisch parsen. `null` wenn ungültig oder nicht in der Map.
 */
export function resolveCidStadt(raw: string | undefined | null): string | null {
  if (!raw) return null
  const n = Number(String(raw).trim())
  if (!Number.isFinite(n) || n <= 0) return null
  return CID_STAEDTE[n] ?? null
}
