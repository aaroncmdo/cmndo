import { chromium } from 'playwright'
import { writeFileSync } from 'node:fs'

const browser = await chromium.launch()
const ctx = await browser.newContext({ viewport: { width: 1440, height: 3000 } })
const page = await ctx.newPage()
await page.goto('http://localhost:3030/login', { waitUntil: 'networkidle' })
await page.fill('input[name="email"]', 'test-kunde@claimondo.de')
await page.fill('input[name="password"]', 'Test1234!')
await page.click('button[type="submit"]')
await page.waitForURL((u) => !u.pathname.includes('/login'), { timeout: 20_000 })
await page.goto('http://localhost:3030/kunde/faelle/65a7640b-62dc-48ca-975f-27c8450477c6', { waitUntil: 'networkidle' })
await page.waitForTimeout(4000)
const finalUrl = page.url()
console.log('Final URL:', finalUrl)
await page.screenshot({ path: 'docs/14.05.2026/leads-konsolidierung-audit/smoke-ui-coverage-screenshots/kunde-debug-full.png', fullPage: true })
const html = await page.content()
writeFileSync('docs/14.05.2026/leads-konsolidierung-audit/smoke-ui-coverage-screenshots/kunde-debug.html', html)
console.log('html length:', html.length)
console.log('Mein Geld:', html.includes('Mein Geld') ? 'yes' : 'no')
console.log('Aus dem Gutachten:', html.includes('Aus dem Gutachten') ? 'yes' : 'no')
console.log('Reparaturkosten:', html.includes('Reparaturkosten') ? 'yes' : 'no')
console.log('Wiederbeschaffung:', html.includes('Wiederbeschaffungswert') ? 'yes' : 'no')
console.log('6.450,69 EUR found:', /6\.450,69|6450,69/.test(html) ? 'yes' : 'no')
console.log('Ihre Forderung:', html.includes('Ihre Forderung') ? 'yes' : 'no')
console.log('Beträge werden nach:', html.includes('werden nach Gutachten') ? 'yes' : 'no')
await browser.close()
